package template;

public class Context {
    private String input;
    private String output;

    public String getInput(){
        return this.input;
    }

    public String getOutput(){
        return this.output;
    }

    public void setInput(String input){
        this.input = input;
    }

    public void setOutput(String output){
        this.output = output;
    }


}
